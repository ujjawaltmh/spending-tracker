    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            java.util.Date dt = d.getDate();
            String s1 = a.getText();
            String s2 = (String)category.getSelectedItem();
            if(d != null && !s1.equals("") && !s2.equals("")){
                java.sql.Date date = new java.sql.Date(dt.getTime());
                int amount = Integer.parseInt(s1);
                db.DbConnect.st.executeUpdate("insert into spendings (category,sdate,amount) values('"+s2+"','"+date+"',"+amount+")");
                JOptionPane.showMessageDialog(null, "Expense added successfully!!");
                getEntries();
            }else{
                JOptionPane.showMessageDialog(null, "Please fill compelte details!!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

